## 3. Почему лишний вес после 50 лет особенно опасен

После менопаузы (климакса) жировая ткань становится главным источником эстрогена (женского гормона). **Чем больше жира, тем больше эстрогена**. А многие виды рака груди растут именно на эстрогене.

Исследования показывают: у женщин с лишним весом после 50 лет риск рака груди выше на **30-50%**.

### Как понять, есть ли лишний вес?

**Индекс массы тела (ИМТ)** = вес (кг) ÷ рост (м)²

- **Норма:** 18.5-24.9
- **Избыточный вес:** 25-29.9
- **Ожирение:** 30 и выше

**Окружность талии:**
- **Норма для женщин:** до 80 см
- **Повышенный риск:** 80-88 см
- **Высокий риск:** больше 88 см

### Что делать:

1. **Измерьте талию** сантиметром на уровне пупка
2. **Встаньте на весы** и посчитайте ИМТ
3. Если показатели выше нормы — запишитесь к терапевту
4. Начните с малого: уберите сладкую газировку, гуляйте 15 минут после ужина

