## Заключение

Защита груди от рака — это не сложно. Это **пять простых правил**, которые можно внедрить в свою жизнь уже сегодня:

1. ✅ **Контролируйте сахар** — меньше сладкого, больше овощей
2. ✅ **Правильные продукты** — половина тарелки овощи, цельнозерновые, рыба
3. ✅ **Здоровый вес** — талия до 80 см, ИМТ до 25
4. ✅ **Движение каждый день** — 150 минут в неделю (20-30 минут в день)
5. ✅ **Хороший сон и спокойствие** — 7-9 часов сна, управление стрессом

**Помните:** даже маленькие изменения имеют значение. Не нужно делать всё идеально сразу. Начните с одного правила, добавьте второе через неделю, третье — через месяц. **Главное — начать.**

