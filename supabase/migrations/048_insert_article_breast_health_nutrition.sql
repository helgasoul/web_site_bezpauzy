## 2. Какие продукты защищают грудь, а какие вредят

### Продукты, которые защищают грудь:

- **Овощи** (особенно зелёные листовые: капуста, шпинат, салат)
- **Фрукты** (ягоды, яблоки, груши)
- **Цельнозерновые** (овсянка, гречка, бурый рис)
- **Рыба** (лосось, скумбрия, сельдь — богаты омега-3)
- **Бобовые** (чечевица, фасоль, нут)
- **Орехи и семена** (грецкие орехи, льняное семя)

### Продукты, которые вредят:

- **Сладости и сахар** (конфеты, печенье, торты, сладкие напитки)
- **Белый хлеб и выпечка** (быстро повышают сахар в крови)
- **Обработанное мясо** (колбасы, сосиски, копчёности)
- **Алкоголь** (даже в малых количествах увеличивает риск)
- **Жареная пища** (особенно на растительном масле при высокой температуре)

### Почему овощи и фрукты защищают?

Они содержат **антиоксиданты** — вещества, которые "тушат" воспаление в организме. Также в них много **клетчатки**, которая помогает контролировать сахар в крови и вес.

### Практические советы:

- **Половина тарелки** должна быть овощами
- **Фрукты** — 2-3 порции в день (1 порция = 1 яблоко или горсть ягод)
- **Белый хлеб** замените на цельнозерновой
- **Сладости** — только по особым случаям, не каждый день

