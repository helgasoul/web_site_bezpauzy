## 4. Движение — это лекарство от рака

**150 минут в неделю** — и риск снижается на **30%**!

Это не я придумала — это результат исследований, в которых участвовали сотни тысяч женщин. Вывод один: **те, кто двигается 150-300 минут в неделю (это 20-40 минут в день), болеют раком груди на 25-30% реже**.

Исследование 2025 года подтвердило: физическая активность помогает:

- Не заболеть раком впервые
- Снизить риск повторной болезни после лечения
- Лучше себя чувствовать во время химиотерапии

### Почему движение защищает от рака?

Объясняю простыми словами, что происходит в организме:

**Сахар и инсулин становятся в норму**

- Когда вы двигаетесь, мышцы "съедают" сахар из крови
- Инсулина нужно меньше (а помните, много инсулина — это "удобрение" для рака?)

**Меньше женских гормонов**

- У женщин после климакса, которые регулярно двигаются, ниже уровень эстрогена в крови
- А многие виды рака груди растут именно на эстрогене

**Сильнее иммунитет**

- Физкультура усиливает клетки-защитники (врачи называют их "естественные киллеры")
- Эти клетки находят и уничтожают раковые клетки
- Меньше воспаления во всём организме

### Не обязательно ходить в спортзал!

Вам не нужны тренажёры или модная спортивная одежда. Подойдёт любое движение:

**Ходьба — самое простое и эффективное:**

- Быстрая прогулка (так, чтобы чуть-чуть участилось дыхание)
- Можно разговаривать, но петь уже тяжело
- Идеально: 30 минут в день, 5 дней в неделю

**Другие варианты:**

- Плавание в бассейне
- Танцы дома под музыку
- Работа в огороде (копание, прополка)
- Катание на велосипеде
- Подъём по лестнице вместо лифта

**Силовые упражнения 2 раза в неделю:**

- Приседания (придерживаясь за стул)
- Отжимания от стола или стены
- Упражнения с небольшими гантелями (1-2 кг) или бутылками с водой
- Подтягивания к потолку

### Очень важно: вставайте каждый час!

Если вы сидите весь день (за швейной машинкой, у телевизора, за компьютером), это очень вредно, даже если вы потом час занимаетесь спортом.

**Поставьте будильник на телефоне:** каждый час вставайте на 2-3 минуты. Пройдитесь, сделайте 5-10 приседаний, потянитесь к потолку.

Исследование 2025 года показало: эти простые перерывы улучшают обмен веществ и снижают сахар в крови, особенно у женщин после климакса.

