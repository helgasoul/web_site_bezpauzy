## Грудное вскармливание — защита на годы

Исследование 2025 года в авторитетном журнале Nature изучило более 6,000 женщин. Вывод поразительный: женщины, которые кормили грудь, имели:

- На **24% ниже** риск диабета в будущем
- На **12% ниже** риск болезней сердца
- **Ниже риск рака груди** (особенно если кормили больше 6 месяцев)

### Почему так?

Когда женщина кормит грудь:

- Клетки груди "созревают" и становятся менее уязвимыми для рака
- Меньше месячных циклов = меньше скачков гормонов
- Изменяется обмен веществ в лучшую сторону

### Если у вас есть или будут дети:

Старайтесь кормить грудь хотя бы 6 месяцев. Каждый месяц кормления — это дополнительная защита на всю жизнь.

