## 5. Сон, спокойствие и витамины

**7-9 часов сна — обязательно!**

Если вы спите меньше 7 часов, риск рака груди повышается на **20-30%**. Почему?

Когда мы спим, организм вырабатывает **мелатонин** — гормон сна. Оказывается, мелатонин:

- Защищает клетки от повреждений
- Помогает иммунитету бороться с раковыми клетками
- Регулирует уровень эстрогена

### Как улучшить сон:

- **Ложитесь и вставайте в одно время** (даже в выходные)
- **За час до сна** выключите телевизор и телефон
- **Спальня должна быть тёмной и прохладной** (18-20°C)
- **Не ешьте за 2-3 часа до сна**
- **Избегайте кофе и алкоголя** вечером

### Стресс и спокойствие

Хронический стресс повышает уровень кортизола (гормон стресса), который:

- Ослабляет иммунитет
- Повышает воспаление
- Может влиять на уровень гормонов

**Что помогает:**

- **Медитация** — 5-10 минут в день
- **Дыхательные упражнения** — вдох 4 сек, задержка 7 сек, выдох 8 сек
- **Прогулки на природе**
- **Общение с близкими**
- **Хобби** — то, что приносит радость

### Витамины и добавки

**⚠️ ВАЖНО:** Витамины эффективны **только при доказанном дефиците**. "На всякий случай" принимать их не стоит — избыток может быть вреден.

**Витамин D:**

- Исследования показывают связь между низким уровнем витамина D и риском рака груди
- **Источники:** солнце (15-20 минут без крема 3-4 раза в неделю), жирная рыба, яичные желтки
- **Добавки:** только если врач определил дефицит по результатам анализов

**Омега-3:**

- Содержится в жирной рыбе (лосось, скумбрия, сельдь)
- Помогает снизить воспаление
- **Добавки:** можно рассмотреть, если не едите рыбу 2-3 раза в неделю

**Что НЕ работает:**

- **Антиоксиданты в таблетках** — исследования не показали пользы, иногда даже вред
- **"Чудо-добавки"** без доказательств — много маркетинга, мало эффективности

