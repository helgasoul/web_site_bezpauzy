#!/bin/bash

# Скрипт для установки зависимостей
# Запустите этот скрипт из директории проекта

echo "Установка зависимостей для проекта Без |Паузы..."
echo ""

if [ ! -f "package.json" ]; then
    echo "❌ Ошибка: package.json не найден!"
    echo "Убедитесь, что вы находитесь в директории проекта."
    exit 1
fi

echo "✅ package.json найден"
echo "Начинаю установку зависимостей..."
echo ""

npm install

if [ $? -eq 0 ]; then
    echo ""
    echo "✅ Зависимости успешно установлены!"
    echo ""
    echo "Теперь вы можете запустить проект:"
    echo "  npm run dev"
else
    echo ""
    echo "❌ Ошибка при установке зависимостей"
    exit 1
fi







